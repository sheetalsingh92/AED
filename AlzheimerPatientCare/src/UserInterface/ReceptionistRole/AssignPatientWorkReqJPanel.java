    public void populatePatient(){
        DefaultTableModel dtm = (DefaultTableModel) tblshowPatientDetails.getModel();
        dtm.setRowCount(0);
        for(Patient p: enterprise.getPatientDirectory().getPatientlist()){
            
                Object row[] = new Object[3];
                row[0] = p;
                row[1] = p.getGuardianName();
                row[2] =  p.getAge();;
             
                   dtm.addRow(row);
            }
            
    }
    

