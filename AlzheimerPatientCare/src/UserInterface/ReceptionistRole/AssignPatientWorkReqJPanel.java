    private void createRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createRequestActionPerformed
        // TODO add your handling code here:
        int row = tblshowPatientDetails.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            Patient patient = (Patient)tblshowPatientDetails.getValueAt(row, 0);
            RequestPatientAreaJPanel panel = new RequestPatientAreaJPanel(userProcessContainer,patient, userAccount, enterprise,enterpriseDirectory,session);
            userProcessContainer.add("RequestPatientAreaJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }

    }//GEN-LAST:event_createRequestActionPerformed

