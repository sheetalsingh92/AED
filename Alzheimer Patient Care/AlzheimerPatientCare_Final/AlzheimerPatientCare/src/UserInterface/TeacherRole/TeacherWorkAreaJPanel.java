    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        WorkRequestTeacher request = (WorkRequestTeacher) workRequestJTable.getValueAt(selectedRow, 0);
        request.setReceiver(userAccount);
        request.setStatusatTeacher("Pending");
        populateTable();

    }//GEN-LAST:event_assignJButtonActionPerformed

    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        WorkRequestTeacher request = (WorkRequestTeacher) workRequestJTable.getValueAt(selectedRow, 0);
        Patient patient = request.getPatient();
        if (request.getReceiver() == null) {
            JOptionPane.showMessageDialog(this, "First assign the request in order to Proceed");
            return;
        }

        if (workRequestJTable.getValueAt(selectedRow, 2) == null) {
            JOptionPane.showMessageDialog(this, "First assign the request in order to Proceed");
            return;
        }
        request.setStatusatTeacher("Processing");
        ProcessWorkRequestJPanelT processWorkRequestJPanel = new ProcessWorkRequestJPanelT(userProcessContainer, request, patient,session);
        userProcessContainer.add("processWorkRequestJPanel", processWorkRequestJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_processJButtonActionPerformed

