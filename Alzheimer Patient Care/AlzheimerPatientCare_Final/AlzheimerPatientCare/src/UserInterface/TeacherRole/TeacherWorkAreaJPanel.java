    public void populateTable() {

        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest request : ((YogaTeacherOrganization) organization).getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[6];
            row[0] = request;
            row[1] = ((WorkRequestTeacher) request).getPatient().getName();
            row[2] = request.getSender().getEmployee().getEmployeeName();
            row[3] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getEmployeeName();
            row[4] = request.getStatusatTeacher();
            row[5] = ((WorkRequestTeacher) request).getSlotconfirmed();
            model.addRow(row);

        }
    }

