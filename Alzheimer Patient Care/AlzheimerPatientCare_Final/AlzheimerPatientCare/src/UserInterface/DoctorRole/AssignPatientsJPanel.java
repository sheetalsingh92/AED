    private void btnViewImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewImageActionPerformed
      
        
        int row = showPatient.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            WorkRequestDoctor wr = (WorkRequestDoctor) showPatient.getValueAt(row, 0);
            if((wr.getPatient().getImage()).length()==0)
            {
             JOptionPane.showMessageDialog(this, "Image not uploaded", "Warning", JOptionPane.WARNING_MESSAGE);
             return;
            }
            else
            lblviewImage.setIcon(new ImageIcon(wr.getPatient().getImage()));

        }
    }//GEN-LAST:event_btnViewImageActionPerformed

