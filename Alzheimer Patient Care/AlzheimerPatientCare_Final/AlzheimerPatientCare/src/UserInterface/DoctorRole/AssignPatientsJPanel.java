    public void populateChild() {
        DefaultTableModel model = (DefaultTableModel) showPatient.getModel();

        model.setRowCount(0);

        for (WorkRequest request : ((DoctorOrganization) organization).getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[6];
            row[0] = request;
            row[1] = ((WorkRequestDoctor) request).getPatient().getName();
            row[2] = request.getSender().getEmployee().getEmployeeName();
            row[3] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getEmployeeName();
            row[4] = request.getStatusatDoctor();
            row[5] = ((WorkRequestDoctor) request).getProblem();

            model.addRow(row);
        }

    }



