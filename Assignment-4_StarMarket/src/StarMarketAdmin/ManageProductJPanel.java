    private void btncreateInventoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncreateInventoryActionPerformed
           Store s = (Store) cmbstoreNames.getSelectedItem(); 
           if(s!=null)
           {
        DefaultTableModel dtm = (DefaultTableModel) tblinventory.getModel();
        dtm.setRowCount(0);
        for (Product p : s.getInventoryDirectory().getInventoryList()) {
            Object row[] = new Object[3];
            row[0] = p.getProductName();
            row[1]= p.getAvailability();
            row[2] = p.getSellingPrice();
           
            dtm.addRow(row);
        }
           }
        else
        {
              JOptionPane.showMessageDialog(this, "Please create a store", "Warning", JOptionPane.WARNING_MESSAGE);
       
        }
       
            
    }//GEN-LAST:event_btncreateInventoryActionPerformed

