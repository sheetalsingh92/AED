    private void btnviewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnviewDetailsActionPerformed
        int selectedRow = tblProductDetails.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a product from the table");
            return;
        }
        Product p = (Product) tblProductDetails.getValueAt(selectedRow, 0);
        UpdateProductsJPanel panel = new UpdateProductsJPanel(p,upc);
        upc.add("UpdateProductsJPanel", panel);
        CardLayout layout = (CardLayout) upc.getLayout();
        layout.next(upc);
    }//GEN-LAST:event_btnviewDetailsActionPerformed

