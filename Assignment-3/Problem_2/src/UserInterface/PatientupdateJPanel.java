    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:

        try {
            String ID = txtID.getText();
            String Name = txtName.getText();
            int Age = Integer.parseInt(txtpersonAge.getText());
            String gender =txtgender.getText();
            String DoctorName = txtdoctorName.getText();

            person.getPatient().setID(ID);
            person.getPatient().setName(Name);
            person.getPatient().setAge(Age);
            person.getPatient().setGender(gender);
            person.getPatient().setDoctorName(DoctorName);
            

            JOptionPane.showMessageDialog(null, "Person details successfully updated.", "Information", JOptionPane.INFORMATION_MESSAGE);
            btnsave.setEnabled(false);
            btnupdate.setEnabled(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Correct format required.", "Information", JOptionPane.ERROR_MESSAGE);
            btnsave.setEnabled(false);
            btnupdate.setEnabled(true);
        }
    }//GEN-LAST:event_btnsaveActionPerformed

