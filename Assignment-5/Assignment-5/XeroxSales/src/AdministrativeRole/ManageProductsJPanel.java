    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
           int selectedRow = tblproductCatalog.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(this, "Please select a product from table to delete");
            return;
        }
        Product p = (Product)tblproductCatalog.getValueAt(selectedRow,0);
        business.getProductCatalog().deleteProduct(p);
       
        JOptionPane.showMessageDialog(this, "Product Details deleted Successfully");   
        refreshtable();
    }//GEN-LAST:event_btndeleteActionPerformed


