    private void refreshtable()
    {
          DefaultTableModel dtm = (DefaultTableModel) tblproductCatalog.getModel();
        dtm.setRowCount(0);
        for (Product p : business.getProductCatalog().getProductList()) {
            Object row[] = new Object[6];
          row[0]= p;
            row[1]= p.getProductName();
            row[2] = p.getCeilingPrice();
            row[3] = p.getTargetPrice();
            row[4] = p.getFloorPrice();
            row[5] = p.getAvailability();
           
            dtm.addRow(row);
    }
        
    }

