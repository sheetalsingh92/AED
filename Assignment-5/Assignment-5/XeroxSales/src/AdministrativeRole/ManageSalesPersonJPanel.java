    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        int selectedRow = tblsalesPersondetails.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(this, "Please select a SalesPerson from table to delete");
            return;
        }
        Product p = (Product)tblsalesPersondetails.getValueAt(selectedRow,0);
        business.getProductCatalog().deleteProduct(p);
       
        JOptionPane.showMessageDialog(this, "Sales Person deleted Successfully");       
           refreshtable();                              

    }//GEN-LAST:event_btndeleteActionPerformed


