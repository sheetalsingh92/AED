    private void refreshtable()
    {
        
         DefaultTableModel dtm = (DefaultTableModel) tblsalesPersondetails.getModel();
        dtm.setRowCount(0);
        for (SalesPerson sp : business.getSalesPersonDirectory().getSalesPersonList()) {
            Object row[] = new Object[3];
          row[0]= sp;
            row[1]= sp.getFirstName();
            row[2] = sp.getLastName();
            
           
            dtm.addRow(row);
        }
        
    }

