    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
      try
      {
     
            Product p =  business.getProductCatalog().addProduct();
            p.setProductName(txtproductName.getText().trim());
            
            String tp=txttargetPrice.getText().trim();
            p.setTargetPrice(Integer.parseInt(tp));
            String cp=txtceilingPrice.getText().trim();
            p.setCeilingPrice(Double.parseDouble(cp));
            String fp=txtfloorprice.getText().trim();
            p.setFloorPrice(Double.parseDouble(fp));
            String a=txtAvailability.getText().trim();
            p.setFloorPrice(Double.parseDouble(a));
            
           
            JOptionPane.showMessageDialog(null, "Product created successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
       
        

        catch(NumberFormatException e)
        {JOptionPane.showMessageDialog(null, "Wrong format", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;

    }//GEN-LAST:event_btnAddProductActionPerformed
    }

